package com.lingtao.ltvideo.base;

public class ImageBean {

    private int res;
    private int w_size;
    private int h_size;


    public ImageBean(int res) {
        this.res = res;
    }

    public int getRes() {
        return res;
    }

    public void setRes(int res) {
        this.res = res;
    }

    public int getW_size() {
        return w_size;
    }

    public void setW_size(int w_size) {
        this.w_size = w_size;
    }

    public int getH_size() {
        return h_size;
    }

    public void setH_size(int h_size) {
        this.h_size = h_size;
    }
}
